    public TelaInicial() {
        initComponents();
        
        btnIniciar.addActionListener(e -> {
            try {
                if(simulacao != null){
                    simulacao.encerrar();
                }
                
                if (txtIntervaloMs.getText().isBlank() || txtQtdVeiculos.getText().isBlank()) {
                    JOptionPane.showMessageDialog(rootPane, "Informe um intervalo de inser√ß√£o e a quantidade m√°xima de ve√≠culos");
                }
                
                String[] malhas = {
                    "malha-exemplo-1.txt", 
                    "malha-exemplo-2.txt", 
                    "malha-exemplo-3.txt", 
                    "malha-teste.txt"
                };
                
                int indexMalha = selMalhas.getSelectedIndex();
                boolean usaSemaforo = (radioGroupExclusaoMutua.getSelection().getActionCommand()).equals("true");
                malha = new Malha("/malhas/" + malhas[indexMalha], usaSemaforo);
                
                int intervaloInsercao = Integer.parseInt(txtIntervaloMs.getText());
                int maxVeiculos = Integer.parseInt(txtQtdVeiculos.getText());
                simulacao = new Simulacao(malha, intervaloInsercao, maxVeiculos);

                malhaPanel.removeAll();
                
                MalhaPanel panel = new MalhaPanel(malha, simulacao);
                malhaPanel.setLayout(new BorderLayout());
                malhaPanel.add(panel, BorderLayout.CENTER);
                malhaPanel.revalidate();
                malhaPanel.repaint();
            } catch (Exception exc) {
                exc.printStackTrace();
            }
            
            simulacao.iniciarInsercao();
            if (!simulacao.isAlive()) simulacao.start();
        });

        btnParar.addActionListener(e -> simulacao.pararInsercao());
        btnEncerrar.addActionListener(e -> simulacao.encerrar());
    }

